    private void silButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silButtonActionPerformed
        bittiYazisi.setText("");
        uyariYazisi.setText("");
        int[] secilenRows = tablo.getSelectedRows();
        int secilenRow = tablo.getSelectedRow();
        
        
        
        if(secilenRow == -1){
            if(model.getRowCount() == 0){
                uyariYazisi.setText("Lütfen silmek için önce çalışan ekleyiniz");
            }
            else{
                uyariYazisi.setText("Lütfen silmek tablodan için çalışan seçiniz");
            }
        }
        else{
            for(int i : secilenRows){
                
               baglanti.bilgiSil(Integer.valueOf(String.valueOf(tablo.getValueAt(i, 0))));
               
        }
            bittiYazisi.setText("Başarıyla silindi");
            bilgileriGetirr();
        }
        
    }//GEN-LAST:event_silButtonActionPerformed


    public void bilgileriGetirr() {
        model.setRowCount(0);
        uyariYazisi.setText("");
        ArrayList<iletisimBilgileri> arrayiletisim = baglanti.bilgileriGetir();
        if(arrayiletisim != null){
            for(iletisimBilgileri iletisimbilgileri : arrayiletisim){
                Object[] objects =  {iletisimbilgileri.getId(),iletisimbilgileri.getIsimsoyisim(),iletisimbilgileri.getYas(),iletisimbilgileri.getTelefon(),iletisimbilgileri.getKangrubu(),iletisimbilgileri.getAdres()};
                model.addRow(objects);
            }
        }
        
        
    }

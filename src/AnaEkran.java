    public boolean eklemeKontrol(){
        ArrayList<String> telefonNo = new ArrayList<String>();
        for(int i = 0 ; i<model.getRowCount() ; i++){
            telefonNo.add(String.valueOf(model.getValueAt(i, 3)));
        }
        String kucukTelefonNo = telefonField.getText().toLowerCase().trim();
        
        for(String s : telefonNo){
                if(s.toLowerCase().equals(kucukTelefonNo)){
                    int secim = JOptionPane.showConfirmDialog(this, "Eklenecek telefon numarası zaten mevcut.\nTekrar eklemek istiyor musunuz ?");
                    if(secim == JOptionPane.YES_OPTION){
                        return true;
                    }
                    else if(secim == JOptionPane.NO_OPTION){
                        return false;
                    }
                    else{
                        uyariYazisi.setText("Lütfen bir seçenek seçiniz.");
                        return false;
                    }
                }
            }
       return  true;
    }
    private void ekleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleButtonActionPerformed
        uyariYazisi.setText("");
        bittiYazisi.setText("");
        
        
        
        String adSoyad = adField.getText().trim();
        String yas = yasField.getText().trim();
        String telefon = telefonField.getText().trim();
        String kanGrubu = String.valueOf(kangrubuBox.getSelectedItem());
        String adres = adresField.getText().trim();
        
        if(adSoyad.equals("") || yas.equals("") || telefon.equals("") || kanGrubu.equals("") || adres.equals("")){
            uyariYazisi.setText("Alanlar boş bırakılamaz!");
        }
        else{
            if(telefon.length() == 11 && eklemeKontrol() && !telefon.contains(" ")){
            if(telefon.startsWith("0")){
            baglanti.bilgiEkle(adSoyad, yas, telefon, kanGrubu,adres);
            bilgileriGetirr();
            bittiYazisi.setText("Başarıyla eklendi.");
            }
            else{
                uyariYazisi.setText("Telefon numarası 0 ile başlamalıdır!");
            }
           
            
        }
            else{
                uyariYazisi.setText("Telefon numarası 11 haneli ve boşluksuz olmalıdır!");
        }
  
        }
        
    }//GEN-LAST:event_ekleButtonActionPerformed

